package com.uumai.crawer.util.license;

/**
 * Created by rock on 12/23/15.
 */
public class LicenseInfo {

    private String user;
    private String company;
    private String email;
    private String edition;
    private String release_version;
    private String valid_until;
    private String distribute_pi_count;

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getEdition() {
        return edition;
    }

    public void setEdition(String edition) {
        this.edition = edition;
    }

    public String getRelease_version() {
        return release_version;
    }

    public void setRelease_version(String release_version) {
        this.release_version = release_version;
    }

    public String getValid_until() {
        return valid_until;
    }

    public void setValid_until(String valid_until) {
        this.valid_until = valid_until;
    }

    public String getDistribute_pi_count() {
        return distribute_pi_count;
    }

    public void setDistribute_pi_count(String distribute_pi_count) {
        this.distribute_pi_count = distribute_pi_count;
    }
}
